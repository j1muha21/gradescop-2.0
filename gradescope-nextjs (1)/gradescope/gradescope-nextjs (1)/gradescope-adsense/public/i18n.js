const LANG_LABELS = { en:'English', de:'Deutsch', fr:'FranÃ§ais', es:'EspaÃ±ol', ar:'Ø§Ù„Ø¹Ø±Ø¨ÙŠØ©', zh:'ä¸­æ–‡', pt:'PortuguÃªs', tr:'TÃ¼rkÃ§e' };

function T(key) {
  const langData = TRANSLATIONS[currentLang] || TRANSLATIONS['en'];
  return langData[key] || TRANSLATIONS['en'][key] || key;
}

function setLanguage(lang, btn) {
  currentLang = lang;
  document.getElementById('currentLangLabel').textContent = LANG_LABELS[lang];
  document.querySelectorAll('.lang-option').forEach(b => b.classList.remove('active'));
  document.querySelectorAll('.mobile-lang-btn').forEach(b => b.classList.remove('active'));
  if (btn) btn.classList.add('active');
  // Sync the other switcher (desktop/mobile)
  document.querySelectorAll('[data-lang="' + lang + '"]').forEach(b => b.classList.add('active'));
  // RTL support for Arabic
  document.documentElement.setAttribute('dir', lang === 'ar' ? 'rtl' : 'ltr');
  document.body.style.direction = (lang === 'ar') ? 'rtl' : 'ltr';
  // Apply translations to all data-i18n elements
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const text = T(key);
    if (!text || text === key) return;
    // Use innerHTML for keys that contain HTML tags
    if (key.includes('headline') || key.includes('Title') || key.includes('title') || text.includes('<')) {
      el.innerHTML = text;
    } else {
      el.textContent = text;
    }
  });
  // Translate <option> elements with data-i18n
  document.querySelectorAll('option[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const text = T(key);
    if (text && text !== key) el.textContent = text;
  });
}

// Expose on window so Layout.js can call it
window.GS_setLanguage = setLanguage;
window.GS_T = T;
window.GS_LANG_LABELS = LANG_LABELS;

// On load, restore saved language
(function() {
  try {
    var lang = localStorage.getItem('gs_lang');
    if (lang && TRANSLATIONS[lang]) {
      setLanguage(lang, null);
    }
  } catch(e) {}
})();
